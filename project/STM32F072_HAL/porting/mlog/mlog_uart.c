/*********************************************************************
 * @fn      fputc
 *
 * @brief   Support Printf Function 
 *
 * @param   data - UART send Data.
 *
 * @return  data - UART send Data.
 */
int fputc(int data, FILE *f)
{
	HAL_UART_Transmit(&huart1, (const uint8_t*)&data, 1, 0xffff);
	return data;
}


